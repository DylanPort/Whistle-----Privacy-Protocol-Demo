pub fn verify_withdraw_proof_groth16(
    proof_a: &[u8; 64],
    proof_b: &[u8; 128],
    proof_c: &[u8; 64],
    commitment: &[u8; 32],
    nullifier_hash: &[u8; 32],
    recipient: &[u8; 32],
    amount: u64,
    relayer_fee: u64,
) -> anchor_lang::Result<bool> {
    msg!("ZK Verify (swapped G2)...");
    
    let mut amount_bytes = [0u8; 32];
    amount_bytes[24..].copy_from_slice(&amount.to_be_bytes());
    
    let mut fee_bytes = [0u8; 32];
    fee_bytes[24..].copy_from_slice(&relayer_fee.to_be_bytes());
    
    let public_inputs: [[u8; 32]; NUM_PUBLIC_INPUTS] = [
        *commitment,
        *nullifier_hash,
        *recipient,
        amount_bytes,
        fee_bytes,
    ];
    
    let vk = get_withdraw_verifying_key();
    
    let mut verifier = Groth16Verifier::<NUM_PUBLIC_INPUTS>::new(
        proof_a, proof_b, proof_c, &public_inputs, &vk,
    ).map_err(|e| { msg!("New: {:?}", e); anchor_lang::error!(crate::WhistleError::InvalidProof) })?;
    
    verifier.prepare_inputs::<true>()
        .map_err(|e| { msg!("Prep: {:?}", e); anchor_lang::error!(crate::WhistleError::InvalidProof) })?;
    
    verifier.verify()
        .map_err(|e| { msg!("Verify: {:?}", e); anchor_lang::error!(crate::WhistleError::InvalidProof) })?;
    
    msg!("âœ… ZK verified!");
    Ok(true)
}
